#This is a list example.

pets = ["cat", "dog", "hamster", "goldfish", "rabbit", "turtle"]
print (pets [1])
print (pets [4])
print (pets [0])

pets.sort()
print("Pets are sorted ", pets)

pets.sort(reverse = True) #descending order
print("Pets are sorted ", pets)

print (pets [1])
print (pets [4])
print (pets [0])
